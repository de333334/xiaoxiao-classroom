async def main():
    manifest = []
    for sid, text in SECTIONS:
        mp3 = os.path.join(OUT, f"{sid}.mp3")
        comm = edge_tts.Communicate(text, VOICE, rate=RATE, pitch=PITCH)
        cues = []
        with open(mp3, "wb") as f:
            async for chunk in comm.stream():
                if chunk["type"] == "audio":
                    f.write(chunk["data"])
                elif chunk["type"] == "SentenceBoundary":
                    start = chunk["offset"] / 1e7
                    end = (chunk["offset"] + chunk["duration"]) / 1e7
                    cues.append({"start": round(start, 3), "end": round(end, 3),
                                 "text": chunk["text"]})
        manifest.append({"id": sid, "mp3": f"audio/{sid}.mp3", "cues": cues, "text": text})
        print("done", sid, os.path.getsize(mp3), "bytes", "cues:", len(cues))
    with open(os.path.join(os.path.dirname(__file__), "manifest.json"), "w", encoding="utf-8") as f:
        json.dump(manifest, f, ensure_ascii=False, indent=2)
    print("ALL DONE")

asyncio.run(main())
